    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
//        1 - LÃª nome 
        String name = nameField.getText();
//        2 - Verifica o estado selecionado
        String state = stateOptions.getSelectedItem().toString();
//        3- Abre uma janela informando os dados
        String message = "";
        
        switch (state) {
            case "RS":
                message = "You are Gaucho!";
                break;
            case "RJ": 
                message = "You are Carioca";
                break;
            case "RN":
                message = "You are Rio Nordense";
        }
        JOptionPane.showMessageDialog(this, message);
    }//GEN-LAST:event_btnRegisterActionPerformed

